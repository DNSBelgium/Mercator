create table vat_crawl_result
(
    id                             int8 generated by default as identity,
    visit_id                       uuid,
    domain_name                    varchar(255),
    start_url                      varchar(255),
    matching_url                   varchar(255),
    crawl_started                  TIMESTAMPTZ,
    crawl_finished                 TIMESTAMPTZ,
    vat_values                     jsonb,
    visited_urls                   jsonb,
    primary key (id)
);


